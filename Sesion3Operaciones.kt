//Operaciones lógicas
//Sirven para comparar valores entre 2 o mas variables

// <  Menor que    
val compar1 = (6 < 5) // Resultado False

// >  Mayor que    
val compar2 = (6 > 5) //Resultado True  

// <= Menor o igual que   
val compar3 = (6 <= 6) //Resultado True     (Cualquier número mayor a 6 dará False como resultado)

// >= Mayor o igual que   
val compar4 = (6 >= 6) // Resultado True     (Cualquier número menor a 6 dará False como resultado)

// == Igual que    
val compar5 = (9 == 9)  // True     (Cualquier otro número dará False como resultado)

// != Diferente que
val compar6 = (9!=9) //False    (Cualquier otro número dará True como resultado)